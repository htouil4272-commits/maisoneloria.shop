const STORAGE_KEY = 'maisoneloria_lang';

export function getCurrentLang() {
  if (typeof window === 'undefined') return 'fr';
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved && translations[saved]) return saved;
  const browserLang = (navigator.language || 'fr').toLowerCase();
  return browserLang.startsWith('ar') ? 'ar' : 'fr';
}

export function setLang(lang) {
  if (!translations[lang]) return;
  localStorage.setItem(STORAGE_KEY, lang);
  applyLang(lang);
}

export function toggleLang() {
  const current = getCurrentLang();
  setLang(current === 'fr' ? 'ar' : 'fr');
}

export function t(key, lang = null) {
  const useLang = lang || getCurrentLang();
  const dict = translations[useLang];
  return key.split('.').reduce((acc, k) => (acc && acc[k] !== undefined ? acc[k] : null), dict) || key;
}

export function applyLang(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', dict.direction);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const value = t(key, lang);
    if (value) {
      if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = value;
      } else {
        el.textContent = value;
      }
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    const value = t(key, lang);
    if (value) el.setAttribute('placeholder', value);
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    const value = t(key, lang);
    if (value) el.setAttribute('aria-label', value);
  });

  document.querySelectorAll('[data-lang-current]').forEach((el) => {
    el.textContent = lang === 'fr' ? 'العربية' : 'Français';
  });

  window.dispatchEvent(new CustomEvent('lang:changed', { detail: { lang } }));
}

export function initI18n() {
  const lang = getCurrentLang();
  applyLang(lang);

  document.querySelectorAll('[data-lang-toggle]').forEach((btn) => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      toggleLang();
    });
  });
}
